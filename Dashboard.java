    private String[] getRowData(javax.swing.JTable tabla) {
        int rowIndex = tabla.getSelectedRow();
        int columnCount = tabla.getColumnCount();
        String[] rowStrings = new String[columnCount];
        for (int i = 0; i < columnCount; i++) {
            rowStrings[i] = (tabla.getModel().getValueAt(rowIndex, i).toString());
        }
        return rowStrings;
    }

    /**
     * Hace reaccionar las labels de los menús
     */
    private void addActionToMenuLabels() {
        Component[] menus = PanelMenu.getComponents();
        for (Component menu : menus) {
            if (menu instanceof JPanel) {
                JPanel contenedor = (JPanel) menu;
                Component[] components = contenedor.getComponents();
                if (menu.getName() != "Header") {
                    for (Component component : components) {
                        JLabel label = (JLabel) component;

                        label.addMouseListener(new MouseListener() {
                            @Override
                            public void mouseClicked(MouseEvent e) {
                                switch (label.getName()) {
                                    case "inicio":
                                        showPanel(Board_Inicio);
                                        break;
                                    case "clientes":
                                        refreshTableCliente();
                                        showPanel(Board_Clientes);
                                        break;
                                    case "facturas":
                                        refreshTableFactura();
                                        showPanel(Board_Facturas);
                                        break;
                                    case "motores":
                                        refreshTableMotor();
                                        showPanel(Board_Motores);
                                        break;
                                    case "vehiculos":
                                        refreshTableVehiculos();
                                        showPanel(Board_Vehiculos);
                                        break;
                                    case "reservas":
                                        refreshTableReserva();
                                        showPanel(Board_Reservas);
                                        break;
                                    case "empleados":
                                        refreshTableEmpleado();
                                        showPanel(Board_Empleados);
                                        break;
                                    case "nominas":
                                        refreshTableNomina();
                                        showPanel(Board_Nominas);
                                        break;
                                    case "concesionarios":
                                        refreshTableConcesionario();
                                        showPanel(Board_Concesionarios);
                                        break;
                                    case "areacliente":
                                        refreshTableAreaCliente();
                                        showPanel(Board_AreaCliente);
                                        break;
                                    case "areaempleado":
                                        refreshTableAreaEmpleado();
                                        showPanel(Board_AreaEmpleados);
                                        break;
                                    default:
                                        showPanel(Board_Inicio);
                                }
                            }

                            @Override
                            public void mousePressed(MouseEvent e) {
                                label.setForeground(new java.awt.Color(171, 171, 27));
                            }

                            @Override
                            public void mouseReleased(MouseEvent e) {
                                label.setForeground(new java.awt.Color(255, 255, 102));
                            }

                            @Override
                            public void mouseEntered(MouseEvent e) {
                                label.setForeground(new java.awt.Color(255, 255, 102));
                            }

                            @Override
                            public void mouseExited(MouseEvent e) {
                                label.setForeground(new java.awt.Color(255, 255, 255));
                            }
                        });
                    }
                }
            }
        }
    }

